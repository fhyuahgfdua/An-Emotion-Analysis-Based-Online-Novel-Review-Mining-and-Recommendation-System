# 黑话映射 (提高权重)
SLANG_MAP = {
    '登味': '吐槽', '咯噔': '吐槽', '意淫': '吐槽', '男凝': '吐槽', '爹味': '吐槽',
    '致郁': '虐点', '治愈': '爽点', '丝滑': '爽点', '神作': '爽点', '烂尾': '吐槽', 
    '意难平': '虐点', 'yyds': '爽点', 'be': '虐点', 'he': '爽点', 'ooc': '吐槽',
    '草蛇灰线': '爽点', '降智': '吐槽', '发刀': '虐点', '上头': '爽点', '下头': '吐槽'
}
def auto_label(text):
    if not isinstance(text, str): return '中性'
    
    # 1. 优先匹配黑话
    for slang, label in SLANG_MAP.items():
        if slang in text:
            return label
            
    # 2. 匹配普通关键词
    scores = {'爽点': 0, '虐点': 0, '吐槽': 0}
    for label, words in LITERATURE_DICT.items():
        for word in words:
            if word in text:
                scores[label] += 1
    
    max_label = max(scores, key=scores.get)
    return max_label if scores[max_label] > 0 else '中性'

def main():
    with app.app_context():
        print("🔄 正在从数据库读取评论...")
        comments = Comment.query.all()
        
        data = []
        for c in comments:
            # 自动预标注
            label = auto_label(c.content)
            data.append({
                'content': c.content,
                'label': label  # 这是机器打的草稿
            })
            
        df = pd.DataFrame(data)
        
        # 导出
        file_name = 'labeled_data.xlsx'
        df.to_excel(file_name, index=False)
        
        print(f"✅ 成功导出 {len(df)} 条数据到 {file_name}")
        print("👉 下一步：打开 Excel，检查 'label' 列，把标错的改对，保存。")

if __name__ == '__main__':
    main()